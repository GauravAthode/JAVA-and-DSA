//Three digit binary conversion of any ten negative numbers
//-312, -514, -213, -825, -911, -221, -133, -125, -418, -713
package december7;
public class BinaryConversion 
{
	public static void main(String[] args) 
	{
		//-312
		int a = 0b11111111111111111111111011001000;
		System.out.println(a);
		//-514
		int b = 0b11111111111111111111110111111110;
		System.out.println(b);
		//-213
		int c = 0b11111111111111111111111100101011;
		System.out.println(c);
		//-825
		int d = 0b11111111111111111111110011000111;
		System.out.println(d);
		//-911
		int e = 0b11111111111111111111110001110001;
		System.out.println(e);
		//-221
		int f = 0b11111111111111111111111100100011;
		System.out.println(f); 
		//-133
		int g = 0b11111111111111111111111101111011;	
		System.out.println(g);
		//-124
		int h = 0b11111111111111111111111110000100;
		System.out.println(h);
		//-418
		int i = 0b11111111111111111111111001011110;
		System.out.println(i);
		//-713
		int j = 0b11111111111111111111110100110111;
		System.out.println(j);                 	

	}

}
