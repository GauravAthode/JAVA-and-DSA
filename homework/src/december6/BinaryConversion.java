//Two digit binary conversion of any ten negative numbers
//-72, -87, -37, -66, -69, -51, -26, -99, -74, -47
package december6;
public class BinaryConversion 
{
	public static void main(String[] args) 
	{
		//-72
		int a = 0b11111111111111111111111110111000;
		System.out.println(a);
		//-87
		int b = 0b11111111111111111111111110101001;
		System.out.println(b);
		//-37
		int c = 0b11111111111111111111111111011011;
		System.out.println(c);
		//-66
		int d = 0b11111111111111111111111110111110;
		System.out.println(d);
		//-69
		int e = 0b11111111111111111111111110111011;
		System.out.println(e);
		//-51
		int f = 0b11111111111111111111111111001101;
		System.out.println(f); 
		//-26
		int g = 0b11111111111111111111111111100110;	
		System.out.println(g);
		//-99
		int h = 0b11111111111111111111111110011101;
		System.out.println(h);
		//-74
		int i = 0b11111111111111111111111110110110;
		System.out.println(i);
		//-47
		int j = 0b11111111111111111111111111010001;
		System.out.println(j);          
	}

}
