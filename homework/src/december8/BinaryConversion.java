//Four digit binary conversion of any ten negative numbers
//-3123, -4221, -6728, -3215, -4266, -7282, -1286, -3296, -7233, -9248
package december8;
public class BinaryConversion 
{
	public static void main(String[] args) 
	{
		//-3123
		int a = 0b11111111111111111111001111001101;
		System.out.println(a);
		//-4221
		int b = 0b11111111111111111110111110000011;
		System.out.println(b);
		//-6728
		int c = 0b11111111111111111110010110111000;
		System.out.println(c);
		//-3215
		int d = 0b11111111111111111111001101110001;
		System.out.println(d);
		//-4266
		int e = 0b11111111111111111110111101010110;
		System.out.println(e);
		//-7282
		int f = 0b11111111111111111110001110001110;
		System.out.println(f); 
		//-1286
		int g = 0b11111111111111111111101011111010;	
		System.out.println(g);
		//-3296
		int h = 0b11111111111111111111001100100000;
		System.out.println(h);
		//-7233
		int i = 0b11111111111111111110001110111111;
		System.out.println(i);
		//-9248
		int j = 0b11111111111111111101101111100000;
		System.out.println(j);                        	


	}

}
