//Five digit binary conversion of any ten negative numbers
//-38652, -82362, -12836, -24282, -16340
package december9;
public class BinaryConversion 
{
	public static void main(String[] args) 
	{
		//-38652
		int a = 0b11111111111111110110100100000100;
		System.out.println(a);
		//-82362
		int b = 0b11111111111111101011111001000110;
		System.out.println(b);
		//-12836
		int c = 0b11111111111111111100110111011100;
		System.out.println(c);
		//-24282
		int d = 0b11111111111111111010000100100110;
		System.out.println(d);
		//-16340
		int e = 0b11111111111111111100000000101100;
		System.out.println(e);
	}

}
