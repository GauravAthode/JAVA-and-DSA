package december6;

public class Binaryconversion 
{

	public static void main(String[] args) 
	{
		//-32
		int a = 0b11111111111111111111111111100000;
		System.out.println(a);
		//-256
		int b = 0b11111111111111111111111100000000;
		System.out.println(b);
		//-210
		int c = 0b11111111111111111111111100101110;
		System.out.println(c);
		//-328
		int d = 0b11111111111111111111111010111000;
		System.out.println(d);
		//-28
		int e = 0b11111111111111111111111111100100;
		System.out.println(e);
		//-89
		int f = 0b11111111111111111111111110100111;
		System.out.println(f);
		
	}

}
